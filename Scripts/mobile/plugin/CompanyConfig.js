function getRecentChineseYears(num) {
    var date = new Date();
    var lastYear = date.getFullYear() - 1;
    var dateArr = [];
    for(var i = 0; i < num; i++) {
        var newYear = lastYear - i;
        dateArr.push(newYear.toString().substring(2, newYear.length) + "年")
    }
    return dateArr.reverse();
}

