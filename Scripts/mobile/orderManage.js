function showDeal(dom){
	var isShow=$(dom).find(".dealWrap").css("display");
	if(isShow=="none"){
		$(".handle .dealWrap").hide();
		$(dom).find(".dealWrap").show();
	}else{
		$(dom).find(".dealWrap").hide();
	}
	
}
